use crate::vec3::Vec3;
use std::f32;

mod ray;
mod vec3;

use ray::Ray;

fn color(r: &Ray) -> Vec3 {
    let unit_direction: Vec3 = Vec3::unit_vector(r.direction());
    let t: f32 = 0.5 * (unit_direction.y() + 1.0);
    (1.0 - t) * Vec3::new(1.0, 1.0, 1.0) + t * Vec3::new(0.5, 0.7, 1.0)
}

